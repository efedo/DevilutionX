struct Missile {
	MissileID _mitype; // Type of projectile
	MissilePosition position;

private:
	int _mimfnum; // The direction of the missile (direction enum)

public:
	int _mispllvl;
	bool _miDelFlag; // Indicate whether the missile should be deleted
	MissileGraphicID _miAnimType;
	MissileGraphicsFlags _miAnimFlags;
	OptionalClxSpriteList _miAnimData;
	int _miAnimDelay; // Tick length of each frame in the current animation
	int _miAnimLen;   // Number of frames in current animation

	// TODO: This field is no longer used and is always equal to
	// (*_miAnimData)[0].width()
	uint16_t _miAnimWidth;

	int16_t _miAnimWidth2;
	int _miAnimCnt; // Increases by one each game tick, counting how close we are to _pAnimDelay
	int _miAnimAdd;
	int _miAnimFrame; // Current frame of animation + 1.
	bool _miDrawFlag;
	bool _miLightFlag;
	bool _miPreFlag;
	uint32_t _miUniqTrans;
	int duration; // Time to live for the missile in game ticks; once 0, the missile will be marked for deletion via _miDelFlag */
	int _misource;
	mienemy_type _micaster;
	int _midam;
	bool _miHitFlag;
	int _midist; // Used for arrows to measure distance travelled (increases by 1 each game tick). Higher value is a penalty for accuracy calculation when hitting enemy
	int _mlid;
	int _mirnd;
	union {
		struct { int var1, var2, var3, var4, var5, var6, var7; }; // Backward compat (offsets 0-24)
		int raw[7];                                  // Raw array access (offsets 0-24)
		struct { int monsterId, state, frameCounter; } guardian; // var1-var3
		struct { int x, y, level; } portal;          // var1-var3: town portal destination
		struct { int runeType, fromX, fromY; } rune; // var1-var3: rune parameters
		struct { int direction, monsterIdx, state; } wall; // var1-var3: wall control
		struct { int range, monsterIdx, damage; } inferno; // var1-var3: inferno
		struct { int centerX, centerY, totalCount; } nova; // var1-var3: nova/immolation
		struct { int srcX, srcY, wallDir; } fireWall; // var1-var3: firewall
		struct { int srcMonsterId, maxHits; } golem;  // var1-var2: golem
		struct { int id; } simple;                    // var1: single-id usage
		struct { int srcX, srcY, pad1, destX, destY; } projectile; // var1-var5: projectile target
		struct { int monsterId, jumps, pad1, destX, destY; } chain; // var1-var5: chain lightning
		struct { int monsterId, pad1, pad2, destX, destY; } lightning; // var1, var4-var5
	};
	bool limitReached;
	int16_t lastCollisionTargetHash; // For moving missiles lastCollisionTargetHash contains the last entity
									 // (player or monster) that was checked in CheckMissileCol (needed to
									 // avoid multiple hits for a entity at the same tile).

	// Was the missile generated by a trap?
	[[nodiscard]] bool IsTrap() const
	{
		return _misource == -1;
	}

	[[nodiscard]] Player *sourcePlayer()
	{
		if (IsNoneOf(_micaster, TARGET_BOTH, TARGET_MONSTERS) || _misource == -1)
			return nullptr;
		return &Players[_misource];
	}

	[[nodiscard]] Monster *sourceMonster()
	{
		if (_micaster != TARGET_PLAYERS || _misource == -1)
			return nullptr;
		return &Monsters[_misource];
	}

	[[nodiscard]] bool isSameSource(Missile &missile)
	{
		return sourceType() == missile.sourceType() && _misource == missile._misource;
	}

	MissileSource sourceType()
	{
		if (_misource == -1)
			return MissileSource::Trap;
		if (_micaster == TARGET_PLAYERS)
			return MissileSource::Monster;
		return MissileSource::Player;
	}

	void setAnimation(MissileGraphicID animtype);

	// Sets the missile sprite to the given sheet frame
	void setFrameGroupRaw(int frameGroup)
	{
		_mimfnum = frameGroup;
		setAnimation(_miAnimType);
	}

	void setDefaultFrameGroup()
	{
		setFrameGroupRaw(0);
	}

	template <typename FrameEnum>
	void setFrameGroup(FrameEnum frameGroup)
	{
		setFrameGroupRaw(static_cast<int>(frameGroup));
	}

	// brief Sets the sprite for this missile so it matches the given facing Direction
	void setDirection(Direction dir)
	{
		setFrameGroupRaw(static_cast<int>(dir));
	}

	// Sets the sprite for this missile so it matches the given Direction16
	// (Desired facing at a 22.8125 degree resolution)
	void setDirection(Direction16 dir)
	{
		setFrameGroupRaw(static_cast<int>(dir));
	}

	int getFrameGroupRaw() const
	{
		return _mimfnum;
	}

	template <typename FrameEnum>
	FrameEnum getFrameGroup() const
	{
		static_assert(std::is_enum_v<FrameEnum>, "Frame group must be an enum");
		return static_cast<FrameEnum>(_mimfnum);
	}

	[[nodiscard]] Direction getDirection() const
	{
		return static_cast<Direction>(_mimfnum);
	}

	[[nodiscard]] Direction16 getDirection16() const
	{
		return static_cast<Direction16>(_mimfnum);
	}
};

