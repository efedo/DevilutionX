struct MissilePosition {
	
	Displacement offset; // Sprite's pixel offset from tile.
	Displacement velocity; // Pixel velocity while moving
	Displacement traveled; // Pixels traveled as a numerator of 65,536.
	WorldTilePosition tile;
	WorldTilePosition start; // Start position
	WorldTilePosition tileForRendering; // Specifies the location (tile) while rendering
	Displacement offsetForRendering; // Specifies the location (offset) while rendering

	// Stops the missile (set velocity to zero and set offset to last renderer location;
	// shouldn't matter because the missile doesn't move anymore)
	void StopMissile()
	{
		velocity = {};
		if (tileForRendering == tile)
			offset = offsetForRendering;
	}
};

